import type { LocaleCode, TranslationDirection } from '@/types/translation'

export const supportedLocales: LocaleCode[] = ['en', 'fr']

export const normalizeLocale = (value: unknown): LocaleCode =>
  value === 'fr' ? 'fr' : 'en'

export const directionLabel = (direction: TranslationDirection, locale: LocaleCode) => {
  if (locale === 'fr') {
    return direction === 'fr-en' ? 'Français vers anglais' : 'Anglais vers français'
  }
  return direction === 'fr-en' ? 'French to English' : 'English to French'
}

